import static java.lang.Math.pow;

public class Prob6{

    public static void main(String[] args) {
        double num1=8;

        double num2=12;

        double power = pow (num1, num2); // Using Math.pow to calculate num1^num2

        System.out.println(num1 + " raised to the power of " + num2 + " is: " + power);

    }

}
